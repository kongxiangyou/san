public class Store {
	private int storeId;
	private int produceNum;
	private int produceId;
	private int allNum;
	private int procurementNum;
	public Store() {
	};

	 

	public Store(int storeId, int produceNum, int produceId, int allNum, int procurementNum) {
		super();
		this.storeId = storeId;
		this.produceNum = produceNum;
		this.produceId = produceId;
		this.allNum = allNum;
		this.procurementNum = procurementNum;
	}



	public int getProcurementNum() {
		return procurementNum;
	}



	public void setProcurementNum(int procurementNum) {
		this.procurementNum = procurementNum;
	}



	public int getAllNum() {
		return allNum;
	}



	public void setAllNum(int allNum) {
		this.allNum = allNum;
	}

	public void setStoreId(int storeId) {
		this.storeId = storeId;
	}

	public int getStoreId() {
		return storeId;
	}

	public void setProduceNum(int produceNum) {
		this.produceNum = produceNum;
	}

	public int getProduceNum() {
		return produceNum;
	}

	public void setProduceId(int produceId) {
		this.produceId = produceId;
	}

	public int getProduceId() {
		return produceId;
	}
}
